void GfxEdit_SetupPalWeb216(GfxEdit_Context *ctx)
{
	u32 c;
	int hi, lo, mx, my, mz;
	int i, j, k;
	
	for(i=0; i<256; i++)
	{
		c=0xFFFF00FFU;
		if(i==255)
			c=0x00FF00FFU;
		ctx->canvas_pal4[i]=c;
	}

	for(i=0; i<16; i++)
	{
		hi=0xAA; lo=0x00;
		if(i&8)
			{ hi=0xFF; lo=0x55; }
		c=0xFF000000U;
		c|=((i&1)?hi:lo)<< 0;
		c|=((i&2)?hi:lo)<< 8;
		c|=((i&4)?hi:lo)<<16;
		
		if(i==6)
			c=0xFFAA5500U;
		ctx->canvas_pal4[i]=c;
		
		mx=i|(i<<4);
		c=(mx<<16)|(mx<<8)|(mx<<0);
		c|=0xFF000000U;
		ctx->canvas_pal4[16+i]=c;
	}

	for(i=0; i<216; i++)
	{
		mx=(i/ 1)%6;
		my=(i/ 6)%6;
		mz=(i/36)%6;
		mx*=51; my*=51; mz*=51;
		c=(mz<<16)|(my<<8)|(mx<<0);
		c|=0xFF000000U;
		ctx->canvas_pal4[32+i]=c;
	}

	ctx->canvas_pal4[32]&=0x00FFFFFFU;

	ctx->canvas_bpp=8;
	GfxEdit_SetupPalFinish(ctx);
}

