void GfxEdit_DrawIcon8px(GfxEdit_Context *ctx, const u32 *ico, int xb, int yb)
{
	u32 pxv;
	int x, y, ci, c, ci_trans;
	
	ci_trans=-1;
	if(((ico[0]>>28)&15)==((ico[7]>>0)&15))
		{ ci_trans=((ico[0]>>28)&15); }
	
	for(y=0; y<8; y++)
	{
		pxv=ico[y];
		for(x=0; x<8; x++)
		{
			ci=(pxv>>((7-x)*4))&15;
			if(ci==ci_trans)
				continue;
			c=gfxedit_uipal[ci];
			if(c&0x8000)
				continue;
			ctx->screen[((yb+y)*320)+(xb+x)]=c;
		}
	}
}

void GfxEdit_DrawCell8px(GfxEdit_Context *ctx, u64 cell,
	int xb, int yb, int clr)
{
	int x, y, z, c;

	c=gfxedit_uipal[clr];
	for(y=0; y<8; y++)
		for(x=0; x<8; x++)
	{
		z=((7-y)*8)+(7-x);
		if(!((cell>>z)&1))
			continue;
		ctx->screen[((yb+y)*320)+(xb+x)]=c;
	}
}

void GfxEdit_DrawClearBox(GfxEdit_Context *ctx, u16 clr,
	int x0, int y0, int x1, int y1)
{
	int x, y;
	
	if(x0>x1)
		{ x=x0; x0=x1; x1=x; }
	if(y0>y1)
		{ y=y0; y0=y1; y1=y; }
	
	if(x0<0)	x0=0;
	if(y0<0)	y0=0;
	if(x1>=320)	x1=319;
	if(y1>=200)	y1=199;
	if((x0>x1) || (y0>y1))
		return;

	for(y=y0; y<=y1; y++)
	{
		for(x=x0; x<=x1; x++)
		{
			ctx->screen[y*320+x]=clr;
		}
	}
}

void GfxEdit_DrawConsole(GfxEdit_Context *ctx)
{
	int x, y, z, c;

	if(!ctx->condown)
		return;

	if((ctx->con_time_ms^ctx->time_ms)&512)
		ctx->redraw_con_dirty=1;

	if(!ctx->redraw_con_dirty)
		return;

	ctx->con_time_ms=ctx->time_ms;
	ctx->redraw_con_dirty=0;

	GfxEdit_DrawClearBox(ctx, gfxedit_uipal[8],
		0, 0, 319, 199);

	for(y=0; y<24; y++)
		for(x=0; x<40; x++)
	{
//		ctx->screen[]
		z=y*40+x;
		c=ctx->conbuf[z];
		GfxEdit_DrawCell8px(ctx, gfxedit_gfxcon_glyphs[c], x*8, y*8, 15);
	}
	
	y=24;
	for(x=0; x<39; x++)
	{
		z=ctx->con_cur_xbase+x;
		c=ctx->conline[z];
		if(!c)break;
		GfxEdit_DrawCell8px(ctx, gfxedit_gfxcon_glyphs[c], (x+1)*8, y*8, 15);
	}
	
	c=']';
	if(ctx->con_cur_xbase!=0)
		c='>';
	GfxEdit_DrawCell8px(ctx, gfxedit_gfxcon_glyphs[c], 0, y*8, 15);
	
	z=ctx->con_cur_pos-ctx->con_cur_xbase;
	if((z>=0) && (z<39) && (ctx->time_ms&512))
	{
		GfxEdit_DrawCell8px(ctx, gfxedit_gfxcon_glyphs['_'], (z+1)*8, y*8, 15);
	}
}

